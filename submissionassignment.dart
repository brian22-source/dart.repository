void main() {
  // int data type example
  int age = 25;
  print('Age: $age');

  // double data type
  double price = 10.99;
  print('Price: \$${price.toStringAsFixed(2)}');

  // String data type
  String name = 'Brian Mbithi';
  print('Name: $name');

  // List data type
  List<int> numbers = [1, 2, 3, 4, 5];
  print('Numbers: $numbers');

  // Map data type example
  Map<String, String> fruits = {
    'apple': 'red',
    'banana': 'yellow',
    'orange': 'orange'
  };
  print('Fruits: $fruits');
}

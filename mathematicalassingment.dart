//Perform all mathematical operations using two variables:

void main() {
  int num1 = 10;
  int num2 = 5;

//Addition: `int addition = num1 + num2;`

  int addition = num1 + num2;

//Subtraction: `int subtraction = num1 - num2;`
  int subtraction = num1 - num2;

  //Multiplication: `int multiplication = num1 * num2;`
  int multiplication = num1 * num2;

//Division: `double division = num1 / num2;
  double division = num1 / num2;
  int modulus = num1 % num2;

  print("Addition: $addition");
  print("Subtraction: $subtraction");
  print("Multiplication: $multiplication");
  print("Division: $division");
  print("Modulus: $modulus");
}

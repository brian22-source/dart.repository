void main() {
  int age = 25;
  int temperature = 75;

  int sum = age + temperature;
  int difference = age - temperature;
  int product = age * temperature;
  double quotient = age / temperature;

  print("Age: $age");
  print("Temperature: $temperature");
  print("Sum: $sum");
  print("Difference: $difference");
  print("Product: $product");
  print("Quotient: $quotient");
}
